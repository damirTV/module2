package homeworks.homework3.ex3;

public class Human {
    String name;
    int age;
    int height;

    public Human(String name, int age, int height) {
        this.name = name;
        this.age = age;
        this.height = height;
    }

    public int getAge() {
        return age;
    }

    public int getHeight() {
        return height;
    }
}
