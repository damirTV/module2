package homeworks.homework3.ex5;

public class Animal {
    Integer lengthBody;
    Integer lengthTail;

    public Animal(Integer lengthBody, Integer lengthTail) {
        this.lengthBody = lengthBody;
        this.lengthTail = lengthTail;
    }

    public Integer getLengthBody() {
        return lengthBody;
    }

    public Integer getLengthTail() {
        return lengthTail;
    }
}
